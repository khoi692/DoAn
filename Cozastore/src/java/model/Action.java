package model;

/**
 *
 * @author Drag
 */
public class Action {
    private String id, action, object, detail, time;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getObject() {
        return object;
    }

    public void setObject(String object) {
        this.object = object;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public Action(String id, String action, String object, String detail, String time) {
        this.id = id;
        this.action = action;
        this.object = object;
        this.detail = detail;
        this.time = time;
    }
    
}
